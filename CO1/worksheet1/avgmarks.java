package worksheet1;
public class avgmarks {
    public static void main(String[] args) {
        // Hardcoded marks for five subjects
        double m1 = 85.5;
        double m2 = 90.0;
        double m3 = 78.5;
        double m4 = 88.0;
        double m5 = 92.5;

        // Calculating average marks
        double averageMarks = (m1 + m2 + m3 + m4 + m5) / 5;

        // Displaying the average marks
        System.out.println("Average Marks: " + averageMarks);
    }
}

